package com.lostagain.Jam.SceneObjects.Interfaces;

import lostagain.nl.spiffyresources.client.spiffycore.Simple2DPoint;

/** an interface that tells the code this object type can recieve UserAction commands
 * All SceneObjects inherit this interface and thus have to have a "userActionTriggeredOnObject" function.
 * Same for InventoryIcons  **/
public interface hasUserActions {

	public void userActionTriggeredOnObject(String userActionsName);

	Simple2DPoint getUpperRight();

	Simple2DPoint getUpperLeft();

	Simple2DPoint getLowerRight();

	Simple2DPoint getLowerLeft();

	Simple2DPoint getLowerLeftDisplacement();
	
}
