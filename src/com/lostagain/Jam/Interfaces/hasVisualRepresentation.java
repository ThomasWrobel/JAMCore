package com.lostagain.Jam.Interfaces;

/**
 * Any class with a visual implementation that the core needs to access should implement this.
 * In GWTs case it is purely for return a object to be then cast a widget.
 * 
 * @author darkflame
 *
 */
public interface hasVisualRepresentation {

	/**
	 * Returns the visual representation of this object ready to be displayed or placed inside a popup
	 * In GWTs implementation this should be a widget returned.
	 * In other cases it should be the "most basic" type of visual object that implementation supports.
	 *  
	 * @return
	 */
	public Object getVisualRepresentation();
	
}
