package com.lostagain.Jam.Interfaces;

public interface HasLoadMonitor {

	/**
	 * advance the loading visualization
	 * (this is the load for the game engine itself, not a specific scene)
	 */
	public void advanceGameLoadingBar();
	
	/**
	 * resets the loading text to the current string
	 * @param text 
	 */
	public void setGameLoadStatus(String text);
	
	
}
